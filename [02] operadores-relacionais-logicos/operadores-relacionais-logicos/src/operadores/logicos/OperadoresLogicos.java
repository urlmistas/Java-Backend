package operadores.logicos;

public class OperadoresLogicos {
    public static void main(String[] args) {
        // Operadores Logicos
        // && (AND -> E), || (OR -> OU), ! (NOT -> NAO)

        boolean resultadoAnd = true && true;
        boolean resultadoOr = true || false;
        boolean resultadoNot = !false;

        System.out.println(resultadoAnd);
        System.out.println(resultadoOr);
        System.out.println(resultadoNot);
    }
}
